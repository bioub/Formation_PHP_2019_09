<?php

$prenom = 'Romain';
var_dump($prenom);

// string -> chaine de caractères -> texte
var_dump('Romain'); // string

// integer -> nombre entier
var_dump(33);

// float -> nombre à virgule
var_dump(3.14);

// boolean -> booléen
// 2 valeurs possibles :
// true -> vrai
// false -> faux
var_dump(true);
var_dump(false);

// array -> tableau
var_dump(['Romain', 'Eric']); // array